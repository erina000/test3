<?php

//【2.PHP　変数・代入・四則演算】//

//変数名の前に$を使用//

//［代入の使い方］//
//高橋と表示//
$name = "高橋";
echo $name;

//10と表示//
$number = 10;
echo $number;

//[「四則演算記号」で計算]//
//6と表示//
$a = 3+3;
echo $a;

//10と表示//
$a = 7+3;
echo $a;

//10と表示//
$b = 2*5;
echo $b;

//5と表示//
$a1 = 10/2;
echo $a1;

//-8と表示//
$a2 = -3-5;
echo $a2;

//計算式は、代入しなくとも、echoだけでもOK。-8と表示//
echo -3-5;

//""を使用し文字列として認識。7+3と表示//
$a = "7+3";
echo $a; 

//2*5と表示//
$b = "2*5"; 
echo $b; 

//-3-5と表示//
echo "-3-5";

?>
